export function getLang() { return currentLang }

export function setLang(lang) {
  currentLang = lang
  localStorage.setItem('lang', lang)
}

export function t(key) {
  return TRANSLATIONS[currentLang]?.[key] ?? TRANSLATIONS.uz[key] ?? key
}

